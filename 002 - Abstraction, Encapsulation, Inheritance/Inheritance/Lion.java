package Inheritance;

public class Lion extends Animal {

    public void eat(){
        System.out.println("Lion is hunting for it's food, kindly wait");
    }

    public void talk(){
        System.out.println("Lion is roaring like a lion to tell the jungle he is the king of it.");
    }

    public void panchayat(){
        System.out.println("Lion is giving a order at the panchayat...");
    }
    
}
